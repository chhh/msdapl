package org.yeastrc.ms.upload.dao;

public class TableCopyException extends Exception {

    public TableCopyException() {
        super();
    }

    public TableCopyException(String message) {
        super(message);
    }

    public TableCopyException(Throwable cause) {
        super(cause);
    }

    public TableCopyException(String message, Throwable cause) {
        super(message, cause);
    }

}
